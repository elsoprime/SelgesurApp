public class Zonales {
    
    private int Id_Zonal;
    private String Zonal;
    private String Comuna;
    private String Provincia;
    private String Región;

    public int getId_Zonal() {
        return Id_Zonal;
    }

    public void setId_Zonal(int Id_Zonal) {
        this.Id_Zonal = Id_Zonal;
    }

    public String getZonal() {
        return Zonal;
    }

    public void setZonal(String Zonal) {
        this.Zonal = Zonal;
    }

    public String getComuna() {
        return Comuna;
    }

    public void setComuna(String Comuna) {
        this.Comuna = Comuna;
    }

    public String getProvincia() {
        return Provincia;
    }

    public void setProvincia(String Provincia) {
        this.Provincia = Provincia;
    }

    public String getRegión() {
        return Región;
    }

    public void setRegión(String Región) {
        this.Región = Región;
    }
    
    
    
}
